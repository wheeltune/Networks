#include "logger.hpp"

#include <iostream>

#include "clock.hpp"


////////////////////////////////////////////////////////////////////////////////

namespace NLogger
{

////////////////////////////////////////////////////////////////////////////////

namespace
{

////////////////////////////////////////////////////////////////////////////////

using NClock::Clock;

////////////////////////////////////////////////////////////////////////////////

}  // namespace

////////////////////////////////////////////////////////////////////////////////

void Logger::LogDebug( const std::string & message ) const
{
    if( log_level_ <= LogLevel::Debug )
        PrintMessage( message );
}

////////////////////////////////////////////////////////////////////////////////

void Logger::LogInfo( const std::string & message ) const
{
    if( log_level_ <= LogLevel::Info )
        PrintMessage( message );
}

////////////////////////////////////////////////////////////////////////////////

void Logger::PrintMessage( const std::string & message ) const
{
    std::cout << "[ " << Clock::FormatTime( Clock::GetTime() ) << " ] "
              << message << std::endl;
}

////////////////////////////////////////////////////////////////////////////////

}  // namespace NLogger

